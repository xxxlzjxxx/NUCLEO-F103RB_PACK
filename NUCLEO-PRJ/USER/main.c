//要写入到W25Q64的字符串数组
const u8 TEXT_Buffer[]={"MiniSTM32 SPI TEST"};
#define SIZE sizeof(TEXT_Buffer)	 
 int main(void)
 { 
	u8 key;
	u16 i = 0, j = 0;
	u8 datatemp[SIZE];
	u32 FLASH_SIZE;
	delay_init();	    	 //延时函数初始化	  
	uart_init(115200);	 	//串口初始化为115200
	LED_Init();		  		//初始化与LED连接的硬件接口
 
	KEY_Init();				//按键初始化		 	
	SPI_Flash_Init();  		//SPI FLASH 初始化

	printf("system init! \r\n"); 	 
		
	while(SPI_Flash_ReadID() != W25Q64)							//检测不到W25Q64
	{
		LED0=!LED0;//DS0闪烁
		delay_ms(500);
	}
	printf("25Q64 Ready!");

	FLASH_SIZE=8*1024*1024;	//FLASH 大小为8M字节  
	
	printf("请按键:(KEY1:写入数据。 KEY0:读取数据。)");
	while(1)
	{
		key=KEY_Scan(0);
		if(key==KEY1_PRES)	//WK_UP 按下,写入W25Q64
		{   
 			printf("Start Write W25Q64....");
			SPI_Flash_Write((u8*)TEXT_Buffer,FLASH_SIZE-100,SIZE);		//从倒数第100个地址处开始,写入SIZE长度的数据
			printf("W25Q64 Write Finished!");	//提示传送完成
		}
		if(key==KEY0_PRES)	//KEY0 按下,读取字符串并显示
		{
 			printf("Start Read W25Q64.... ");
			SPI_Flash_Read(datatemp,FLASH_SIZE-100,SIZE);				//从倒数第100个地址处开始,读出SIZE个字节
			printf("The Data Readed Is:  ");	//提示传送完成
			for(j = 0; j < sizeof(datatemp); j++)
			{
				printf(" %c",datatemp[j]);					//显示读到的字符串
			}
		}
		i++;
		delay_ms(10);
		if(i==20)
		{
			LED0=!LED0;//提示系统正在运行	
			i=0;
		}		   
	}
}

